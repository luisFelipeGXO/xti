package br.com.xti.logica;
/**
* Apresentar os conceitos de Variável
* @autor Luís Felipe
*/
public class variavel {
	
	public static void main (String[] args) {
		
		String nome = "Nome";
		int idade = 16;
		boolean casado = true;
		double preco = new double(12.45);
		char sexo = 'M';
		
		byte b = 127; // 100
		short s = 32767; // 32 mil
		int i = 2000000000; // 2 bilhões
		long l = 9000000000000000000; // 9 quintilhões
		
		double d = 1.79769313486623157E+308; // IEEE 754
		byte bb = 0b01010101; // 8 bits ou 1 byte
		
	}
	
}
